package com.bhavesh.operators;

public class ArithmeticsOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // addition
        System.out.println("addition____");
        int c = a + b;
        System.out.println("sum is =" + c);

        // Subtraction
        System.out.println("subtraction___");
        int d = a - b;
        System.out.println("Subtraction is :" + d);

        // multiplication
        System.out.println("Multiplication___");
        int e = a * b;
        System.out.println("multiplication is :" + e);

        // division
        System.out.println("Division___");
        int f = a/b;
        System.out.println("division is :" + f);

    }
}